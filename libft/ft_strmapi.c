#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*dest;
	unsigned int	a;

	if (!s || !f)
		return (NULL);
	dest = malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (!dest)
		return (NULL);
	a = 0;
	while (s[a])
	{
		dest[a] = f(a, s[a]);
		a++;
	}
	dest[a] = '\0';
	return (dest);
}
